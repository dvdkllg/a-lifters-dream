export class NotificationService {
  private static instance: NotificationService;
  private hasPermission = false;
  private lastAppOpen = Date.now();

  private constructor() {}

  static getInstance(): NotificationService {
    if (!NotificationService.instance) {
      NotificationService.instance = new NotificationService();
    }
    return NotificationService.instance;
  }

  async requestPermissions(): Promise<boolean> {
    if (!Capacitor.isNativePlatform()) {
      console.log('Not running on native platform, notifications not available');
      return false;
    }

    try {
      const permission = await LocalNotifications.requestPermissions();
      this.hasPermission = permission.display === 'granted';
      return this.hasPermission;
    } catch (error) {
      console.error('Failed to request notification permissions:', error);
      return false;
    }
  }

  updateLastAppOpen(): void {
    this.lastAppOpen = Date.now();
    localStorage.setItem('lastAppOpen', this.lastAppOpen.toString());
  }

  private getLastAppOpen(): number {
    const stored = localStorage.getItem('lastAppOpen');
    return stored ? parseInt(stored) : Date.now();
  }

  private shouldSendNotification(): boolean {
    const lastOpen = this.getLastAppOpen();
    const twoDaysAgo = Date.now() - (2 * 24 * 60 * 60 * 1000);
    return lastOpen < twoDaysAgo;
  }

  private getRandomMessage(isHarsh: boolean): string {
    const messages = isHarsh ? HARSH_MOTIVATIONAL_MESSAGES : MOTIVATIONAL_MESSAGES;
    const randomIndex = Math.floor(Math.random() * messages.length);
    return messages[randomIndex];
  }

  async scheduleMotivationalNotification(isHarsh: boolean = false): Promise<void> {
    if (!this.hasPermission || !Capacitor.isNativePlatform()) {
      return;
    }

    if (!this.shouldSendNotification()) {
      return;
    }

    try {
      const message = this.getRandomMessage(isHarsh);
      
      await LocalNotifications.schedule({
        notifications: [
          {
            title: "Time to Get Back in There!",
            body: message,
            id: Date.now(),
            schedule: { at: new Date(Date.now() + 1000) }, // 1 second from now for testing
            sound: 'beep.wav',
            attachments: undefined,
            actionTypeId: "",
            extra: null
          }
        ]
      });
    } catch (error) {
      console.error('Failed to schedule notification:', error);
    }
  }

  startMotivationalReminders(isEnabled: boolean, isHarsh: boolean = false): void {
    if (!isEnabled) {
      return;
    }

    // Check every hour if we should send a notification
    setInterval(() => {
      this.scheduleMotivationalNotification(isHarsh);
    }, 60 * 60 * 1000); // 1 hour
  }
}
